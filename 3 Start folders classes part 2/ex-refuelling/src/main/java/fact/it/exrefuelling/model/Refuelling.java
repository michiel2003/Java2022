package fact.it.exrefuelling.model;

public class Refuelling {
	
	int previousMileage;
	int currentMileage;
	double ammountInLiters;
	
	
	
	public int getPreviousMileage() {
		return previousMileage;
	}
	public void setPreviousMileage(int previousMileage) {
		this.previousMileage = previousMileage;
	}
	public int getCurrentMileage() {
		return currentMileage;
	}
	public void setCurrentMileage(int currentMileage) {
		this.currentMileage = currentMileage;
	}
	public double getAmmountInLiters() {
		return ammountInLiters;
	}
	public void setAmmountInLiters(double ammountInLiters) {
		this.ammountInLiters = ammountInLiters;
	}
	
	

}
